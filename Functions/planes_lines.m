function [P, L] = planes_lines (V)
% Planes
P=zeros(3,3,6);
P(:,:,1) = [V(:,1), V(:,2), V(:,3)];%Left
P(:,:,2) = [V(:,1), V(:,5), V(:,4)];%Right
P(:,:,3) = [V(:,1), V(:,2), V(:,5)];%Up
P(:,:,4) = [V(:,1), V(:,3), V(:,4)];%Down
P(:,:,5) = [V(:,2), V(:,3), V(:,4)];%Back1
P(:,:,6) = [V(:,2), V(:,5), V(:,4)];%Back2

%Lines
L=zeros(3,2,8);
for i=2:5
    L(:,:,i-1)=[V(:,1) V(:,i)];
end
L(:,:,5)=[V(:,2) V(:,3)];
L(:,:,6)=[V(:,3) V(:,4)];
L(:,:,7)=[V(:,4) V(:,5)];
L(:,:,8)=[V(:,5) V(:,2)];

end
